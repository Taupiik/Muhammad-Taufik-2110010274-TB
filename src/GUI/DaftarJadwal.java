    private void btnFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterActionPerformed
        // Simpan nilai filter ke variabel
        dariFilter = cmbDari.getSelectedItem().toString();
        keFilter = cmbKe.getSelectedItem().toString();

        // Muat data dari database berdasarkan filter
        muatDataDariDatabase("", dariFilter, keFilter);

        // Mengecek apakah ada data yang ditemukan
        DefaultTableModel modelTabel = (DefaultTableModel) tblJadwal.getModel();
        if (modelTabel.getRowCount() == 0) {
            getToolkit().beep();
            JOptionPane.showMessageDialog(this, "Data Penerbangan Belum Ada", "Info", JOptionPane.INFORMATION_MESSAGE);
            btnClearActionPerformed(evt);
        }
    }//GEN-LAST:event_btnFilterActionPerformed

    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        // Membersihkan nilai filter
        dariFilter = "";
        keFilter = "";

        // Mengosongkan combobox
        cmbDari.setSelectedIndex(0);
        cmbKe.setSelectedIndex(0);

        // Mengosongkan textfield pencarian
        tfCari.setText("");

        // Memuat kembali semua data dari database
        muatDataDariDatabase("", "", "");
    }//GEN-LAST:event_btnClearActionPerformed

