    private void muatDataDariDatabase(String kataKunci, String dari, String ke) {
        String sql = "SELECT id_jadwal, pesawat.nama_pesawat, kota_awal, kota_tujuan, jam_berangkat, jam_sampai, harga "
                    + "FROM jadwal "
                    + "INNER JOIN pesawat ON jadwal.id_pesawat = pesawat.id_pesawat "
                    + "WHERE (kota_awal LIKE ? OR kota_tujuan LIKE ? OR pesawat.nama_pesawat LIKE ? OR jam_berangkat LIKE ? OR jam_sampai LIKE ?) ";

        if (!dari.isEmpty() && !ke.isEmpty()) {
            sql += "AND kota_awal = ? AND kota_tujuan = ? ";
        }

        sql += "ORDER BY id_jadwal";

        try {
            Connection koneksi = config.configDB();
            PreparedStatement statemen = koneksi.prepareStatement(sql);

            // Mengatur parameter dengan kata kunci pencarian
            statemen.setString(1, "%" + kataKunci + "%");
            statemen.setString(2, "%" + kataKunci + "%");
            statemen.setString(3, "%" + kataKunci + "%");
            statemen.setString(4, "%" + kataKunci + "%");
            statemen.setString(5, "%" + kataKunci + "%");

            if (!dari.isEmpty() && !ke.isEmpty()) {
                // Set parameter untuk filter kota asal dan kota tujuan
                statemen.setString(6, dari);
                statemen.setString(7, ke);
            }

            ResultSet hasil = statemen.executeQuery();

            DefaultTableModel modelTabel = (DefaultTableModel) tblJadwal.getModel();

            // Hapus baris yang sudah ada
            modelTabel.setRowCount(0);

            while (hasil.next()) {
                Object[] data = {
                        hasil.getString("id_jadwal"),
                        hasil.getString("nama_pesawat"),
                        hasil.getString("kota_awal"),
                        hasil.getString("kota_tujuan"),
                        hasil.getString("jam_berangkat"),
                        hasil.getString("jam_sampai"),
                        hasil.getString("harga")
                };
                modelTabel.addRow(data);
            }

            // Menutup koneksi ke database
            koneksi.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error saat memuat data dari database: " + e.getMessage());
        }
    }
    
    private void isiComboBoxDariTabelKota() {
        try {
            Connection koneksi = config.configDB();
            String sql = "SELECT nama_kota FROM kota";
            PreparedStatement statement = koneksi.prepareStatement(sql);
            ResultSet result = statement.executeQuery();

            // Mengosongkan combobox
            cmbDari.removeAllItems();
            cmbKe.removeAllItems();

            // Mengisi combobox dengan data dari tabel kota
            while (result.next()) {
                cmbDari.addItem(result.getString("nama_kota"));
                cmbKe.addItem(result.getString("nama_kota"));
            }

            // Menutup koneksi ke database
            koneksi.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error saat mengisi combobox: " + e.getMessage());
        }
    }
    
